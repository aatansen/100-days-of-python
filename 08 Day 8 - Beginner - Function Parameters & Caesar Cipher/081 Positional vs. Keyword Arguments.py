# Positional argument:
def greet_with(name,location):
    print(f"Hello {name}")
    print(f"What is it like in {location}")
greet_with("Tansen","Bangladesh")

# Keyword argument:
def greet_with(name,location):
    print(f"Hello {name}")
    print(f"What is it like in {location}")
greet_with(name="Tansen",location="Bangladesh")
